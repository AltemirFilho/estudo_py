# (try) e (except) como maneira de evitar o sitema crashar
# Usamos o (try) no início de um bloco de ordem, ele diz ao python "Tente executar este bloco de código. Ele é arriscado e pode dar erro."
# O (except) ao final do bloco serve como um "anticrash", ele diz ao python "Tente executar este bloco de código. Ele é arriscado e pode dar erro."
