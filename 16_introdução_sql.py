# Vamos agoara ver uma introdução ao SQL, que é uma linguagem de consulta estruturada usada para gerenciar bancos de dados relacionais. 
# SQL é fundamental para interagir com dados armazenados em tabelas, permitindo realizar operações como inserção, atualização, exclusão e consulta de dados.
# SQl é uma linguagem declarativa, o que significa que você descreve o que deseja fazer com os dados, e não como fazê-lo.
# A vantagem é que o SQL pode ser usado no python através de bibliotecas como sqlite3, que permite criar e manipular bancos de dados SQLite diretamente do Python.

